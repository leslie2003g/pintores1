    <p><img class="main" src="images/jackson.jpg" alt="Upss! La imagen lo carga :)" align="left"><center><b><i><h1>Reseña:</h1></i></b></center><br><br><b>Jackson Pollock</b> nació en Cody, Estados Unidos, 1912 y murió en Springs, id., 1956 fue un pintor estadounidense. Durante su infancia y su adolescencia vivió en Arizona y California, y en 1929 se trasladó a Nueva York para estudiar pintura con Benton en el Art Students League. Durante su período de formación conoció la pintura de los muralistas mexicanos (Rivera, Siqueiros, Orozco), que le impresionó hondamente.
    <br>
    <br>
    Comenzó su carrera con obras figurativas, en las que presta ya particular atención a los valores matéricos y el cromatismo. Hacia 1938 empezó a interesarse por la pintura abstracta e irracional, y para las obras de este período buscó inspiración en el mundo de los indios americanos.
    <br>
    <br>
    El año 1947 fue decisivo en su trayectoria, ya que fue cuando adoptó la peculiar técnica del <u>dripping:</u> en lugar de utilizar caballete y pinceles, colocaba en el suelo el lienzo y sobre él vertía o dejaba gotear la pintura, que manipulaba después con palos u otras herramientas, e incluso a veces le daba una gran consistencia mediante la adición de arena o fragmentos de vidrio.
    <br>
    <br>
    Gracias al apoyo de algunos críticos como Harold Rosenberg, su nombre, asociado a las obras realizadas con la técnica del dripping, se convirtió en uno de los más significativos del expresionismo abstracto y de la action painting, tendencia de la que, con Willem de Kooning, es el representante más típico y destacado. Fue además uno de los primeros artistas en eliminar de sus obras el concepto de composición y en mezclar signos caligráficos con los trazos pictóricos.
    <br>
    <br>
    El pintor estadounidense Jackson Pollock es mejor conocido por su innovadora técnica de pintura. Al verter pintura sobre el lienzo, el expresionista abstracto creó obras de arte dinámicas y abstractas que revolucionaron la escena artística. Pollock utilizó la fuerza de todo su cuerpo para salpicar y verter pintura sobre sus lienzos, poniendo de cabeza las nociones tradicionales de cómo debían trabajar los pintores.</p>
