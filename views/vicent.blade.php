    <p><img class="dos" src="images/vicente.jpg" alt="Upss! La imagen lo carga :)" align="left"><center><b><i><h1>Reseña:</h1></i></b></center><br><br><b>Vincent Willem van Gogh, nació en Zundert, Países Bajos; el 30 de marzo del año 1853.</b> Hijo de un pastor protestante llamado Theodorus van Gogh y de Anna Cornelia Carbentus, de cuyo matrimonio nacieron 5 hijos.
    <br>
    <br>
    Cursó sus estudios hasta los 15 años en el <b>Instituto Hannik Tilburg.</b> Intentó hacerse teólogo en 1877, pero por no saber ni griego, ni latín, fue rechazado. Fue enviado a Bélgica como misionero a la región de Mons entre los mineros de Borinage.
    <br>
    <br>
    Sus primeras composiciones revelan sus experiencias como predicador, en la tosa y directa Los comedores de papas, siendo <b>uno de los diez únicos grabados que hizo durante su carrera.</b>
    <br>
    <br>
    <b>Viajo a París en 1866, </b>lugar donde empezó a vivir con su hermano Théo, quien era marchante de arte y fue allí, que comenzó a familiarizarse con las nuevas propuestas artísticas que se encontraban en auge. La influencia de Ando Hiroshige y Hokusai, entre otros, lo motivó a experimentar con las técnicas del momento.
    <br>
    <br>
    Tenía la ilusión de fundar un <b>Taller del Mediodía,</b> por esta razón en 1888 abandonó París y se mudó al sur de Francia, con la idea de entusiasmar a sus amigos para seguir su ideal. Se entregó por completo a la pintura, creando diversas obras y usando como inspiración el sol ardiente de la Provenza, dibujaba con tal pasión, transmitiendo todo su sentimiento en cada obra.
    <br>
    <br>
    Aunque hoy es visto como uno de los pintores más influyentes del arte occidental, Vincent van Gogh no tuvo éxito comercial durante su vida. Sin embargo, desde principios del siglo XX, sus expresivas pinturas han cautivado al mundo del arte. Llenas de pinceladas dramáticas y colores llamativos, la obra de este pintor postimpresionista es una hoja de ruta hacia el arte moderno.</p>
