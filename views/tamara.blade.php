    <p><img class="main" src="images/tamara.png" alt="Upss! La imagen lo carga :)" align="left"><center><b><i><h1>Reseña:</h1></i></b></center><br><br><b>Tamara de Lempicka o Tamara Lempicka:</b> Nacida el 16 de mayo de 1898, Maria Górska, Varsovia, Polonia, en el seno de una familia acaudalada, siendo desde pequeña una niña autoritaria y con carácter. En 1910 pinta su primer trabajo, el retrato de su hermana. En 1911 hace con su abuela un viaje a Italia, donde descubre su pasión por el arte.
    <br>
    <br>
    En 1916 se casó en San Petersburgo con el abogado polaco Tadeusz Łempicki (1888-1951), con quien llevó una vida lujosa hasta que estalló la Revolución de octubre. Tadeusz resultó encarcelado, pero ella lo sacó de prisión y se trasladaron a Copenhague. Se mudaron a París, donde nació su hija, Kizette. Tamara tomó clases de pintura con André Lhote. Más tarde expuso en varias galerías de París, ya en estilo art decó, como en la galería Colette Weill. En 1925 tuvo un lugar en la <b>primera exposición art decó de París,</b> donde se hizo un nombre como artista.
    <br>
    <br>
    En 1960, Tamara cambia de estilo, pasándose al abstraccionismo. Tamara de Lempicka recuerda en su autorretrato 1929 <b>"Tamara en Bugatti verde", la trágica muerte de la bailarina estadounidense Isadora Duncan que murió estrangulada en 1927,</b> cuando su largo chal se enreda en una de las ruedas posteriores de su Bugatti.
    <br>
    <br>
    La artista polaca Tamara de Lempicka fue una superestrella de principios del siglo XX. Rodeada por la élite de la vanguardia en París, esta pintora se distanció del impresionismo, el estilo popular de la época, y se concentró en trazar su propio camino. La calidad gráfica de su trabajo y sus colores vivos e intensos la convirtieron en una líder de la pintura art déco. Hoy, su trabajo continúa inspirando a los diseñadores y es emblemático de esta Edad de Oro.</p>
